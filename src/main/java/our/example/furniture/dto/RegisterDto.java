package our.example.furniture.dto;

public class RegisterDto {
	private String registerId;
	private String registerPassword;
	private String registerRePassword;
	private String registerName;
	private String registerBirth;
	private String registerGender;
	private String registerEmail;
	private String registerPhone;
	
	public String getRegisterId() {
		return registerId;
	}
	public void setRegisterId(String registerId) {
		this.registerId = registerId;
	}
	public String getRegisterPassword() {
		return registerPassword;
	}
	public void setRegisterPassword(String registerPassword) {
		this.registerPassword = registerPassword;
	}
	public String getRegisterRePassword() {
		return registerRePassword;
	}
	public void setRegisterRePassword(String registerRePassword) {
		this.registerRePassword = registerRePassword;
	}
	public String getRegisterName() {
		return registerName;
	}
	public void setRegisterName(String registerName) {
		this.registerName = registerName;
	}
	public String getRegisterBirth() {
		return registerBirth;
	}
	public void setRegisterBirth(String registerBirth) {
		this.registerBirth = registerBirth;
	}
	public String getRegisterGender() {
		return registerGender;
	}
	public void setRegisterGender(String registerGender) {
		this.registerGender = registerGender;
	}
	public String getRegisterEmail() {
		return registerEmail;
	}
	public void setRegisterEmail(String registerEmail) {
		this.registerEmail = registerEmail;
	}
	public String getRegisterPhone() {
		return registerPhone;
	}
	public void setRegisterPhone(String registerPhone) {
		this.registerPhone = registerPhone;
	}
}
